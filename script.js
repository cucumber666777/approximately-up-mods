function openUploadDatabase() {
  return new Promise((resolve, reject) => {
    const request = indexedDB.open("ApproximatelyUpModsUploads", 1);
    request.onupgradeneeded = () => {
      const db = request.result;
      if (!db.objectStoreNames.contains("mods")) db.createObjectStore("mods", { keyPath: "id" });
    };
    request.onsuccess = () => resolve(request.result);
    request.onerror = () => reject(request.error);
  });
}

async function saveUploadedModRecord(record) {
  const db = await openUploadDatabase();
  await new Promise((resolve, reject) => {
    const tx = db.transaction("mods", "readwrite");
    tx.objectStore("mods").put(record);
    tx.oncomplete = resolve;
    tx.onerror = () => reject(tx.error);
  });
  db.close();
}

async function getUploadedModRecords() {
  const db = await openUploadDatabase();
  const records = await new Promise((resolve, reject) => {
    const tx = db.transaction("mods", "readonly");
    const request = tx.objectStore("mods").getAll();
    request.onsuccess = () => resolve(request.result || []);
    request.onerror = () => reject(request.error);
  });
  db.close();
  return records;
}


async function deleteUploadedModRecord(id) {
  const db = await openUploadDatabase();
  await new Promise((resolve, reject) => {
    const tx = db.transaction("mods", "readwrite");
    tx.objectStore("mods").delete(id);
    tx.oncomplete = resolve;
    tx.onerror = () => reject(tx.error);
  });
  db.close();
}
function revokeUploadObjectUrls() {
  uploadObjectUrls.forEach((url) => URL.revokeObjectURL(url));
  uploadObjectUrls = [];
}

async function loadUploadedMods() {
  revokeUploadObjectUrls();
  const records = await getUploadedModRecords();
  uploadedMods = records.sort((a, b) => b.createdAt - a.createdAt).map((record) => {
    const fileUrl = URL.createObjectURL(record.fileBlob);
    uploadObjectUrls.push(fileUrl);
    const screenshots = (record.screenshots || []).map((shot, index) => {
      const image = URL.createObjectURL(shot.blob);
      uploadObjectUrls.push(image);
      const title = shot.name || `Screenshot ${index + 1}`;
      return { image, title: { ru: title, en: title } };
    });
    return {
      id: record.id,
      name: record.name,
      category: record.category,
      version: "pending",
      gameBuild: record.gameBuild,
      loader: "MelonLoader",
      status: "warn",
      summary: { ru: record.description, en: record.description },
      longDescription: { ru: record.description, en: record.description },
      screenshots,
      file: fileUrl,
      fileName: record.fileName,
      canDelete: true,
      owner: "local-upload"
    };
  });
}

function sanitizeStorageName(name) {
  return String(name || "file")
    .toLowerCase()
    .replace(/[^a-z0-9._-]+/g, "-")
    .replace(/^-+|-+$/g, "") || "file";
}

function getPublicStorageUrl(bucket, path) {
  const { data } = supabaseClient.storage.from(bucket).getPublicUrl(path);
  return data?.publicUrl || "#";
}

async function getSupabaseUser() {
  if (!supabaseClient) return null;
  const { data, error } = await supabaseClient.auth.getUser();
  if (error) return null;
  return data.user || null;
}

async function uploadFileToSupabase(bucket, userId, file, prefix = "") {
  const cleanName = sanitizeStorageName(file.name);
  const path = `${userId}/${Date.now()}-${prefix}${cleanName}`;
  const { error } = await supabaseClient.storage.from(bucket).upload(path, file, {
    cacheControl: "3600",
    upsert: false
  });
  if (error) throw error;
  return { path, url: getPublicStorageUrl(bucket, path) };
}

async function publishModToSupabase(record, userOverride = null) {
  if (!supabaseClient) throw new Error(t("supabaseMissing"));
  const user = userOverride || await getSupabaseUser();
  if (!user) throw new Error(t("loginRequired"));

  const modFile = await uploadFileToSupabase("mod-files", user.id, record.fileBlob);
  const screenshots = [];
  for (let index = 0; index < record.screenshots.length; index += 1) {
    const shot = record.screenshots[index];
    const uploaded = await uploadFileToSupabase("mod-screenshots", user.id, shot.blob, `${index + 1}-`);
    screenshots.push({ image: uploaded.url, path: uploaded.path, title: { ru: shot.name, en: shot.name } });
  }

  const payload = {
    author_id: user.id,
    name: record.name,
    category: record.category,
    version: "1.0.0",
    game_build: record.gameBuild,
    loader: "MelonLoader",
    status: "warn",
    summary_ru: record.description,
    summary_en: record.description,
    description: record.description,
    file_url: modFile.url,
    file_path: modFile.path,
    screenshots,
    published: false
  };

  const { error } = await supabaseClient.from("mods").insert(payload);
  if (error) throw error;
}
